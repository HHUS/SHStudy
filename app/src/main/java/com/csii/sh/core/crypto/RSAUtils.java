    /**
     * 加密字符串
     *
     * @param encryptString
     * @return
     */
    public static String encrypt(String encryptString) {

        PublicKey publicKey = null;
        try {
            publicKey = loadPublicKey(publicKeyBase64);
        } catch (Exception e) {
            e.printStackTrace();
        }
        byte[] encryptData = FormatTools.getInstance().String2Bytes(encryptString);
        byte[] encryptByte = RSAUtils.encryptData(encryptData, publicKey);

        String afteEncryptr = Base64Utils.encode(encryptByte); //base处理
        return afteEncryptr;
    }

    /**
     * 解密字符串
     *
     * @param encryptedBase64String
     * @return
     */
    public static String decrypt(String encryptedBase64String) {
        PrivateKey privateKey = null;
        try {
            privateKey = loadPrivateKey(privateKeyBase64);
        } catch (Exception e) {
            e.printStackTrace();
        }
        byte[] decryptByte = RSAUtils.decryptData(Base64Utils.decode(encryptedBase64String), privateKey);
        String decrptStr = new String(decryptByte);
        return decrptStr;
    }


